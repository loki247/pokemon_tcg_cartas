package com.example.pokemontcg.model_bkp.tcg;

public class CardCount {
    private Integer firstEd;
    private Integer holo;
    private Integer normal;
    private Integer official;
    private Integer reverse;
    private Integer total;

    public Integer getFirstEd() {
        return firstEd;
    }

    public void setFirstEd(Integer firstEd) {
        this.firstEd = firstEd;
    }

    public Integer getHolo() {
        return holo;
    }

    public void setHolo(Integer holo) {
        this.holo = holo;
    }

    public Integer getNormal() {
        return normal;
    }

    public void setNormal(Integer normal) {
        this.normal = normal;
    }

    public Integer getOfficial() {
        return official;
    }

    public void setOfficial(Integer official) {
        this.official = official;
    }

    public Integer getReverse() {
        return reverse;
    }

    public void setReverse(Integer reverse) {
        this.reverse = reverse;
    }

    public Integer getTotal() {
        return total;
    }

    public void setTotal(Integer total) {
        this.total = total;
    }
}
